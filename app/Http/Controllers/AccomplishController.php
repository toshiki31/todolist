    {
        //$idはtaskテーブルのid
        $flag=Task::where('id',$id)->value('isfinished');
        if($flag==false){
            Task::where('id',$id)->update(['isfinished'=>1]);
        }else{
            Task::where('id',$id)->update(['isfinished'=>0]);
        }
        return redirect()->route('task.index');
    }

